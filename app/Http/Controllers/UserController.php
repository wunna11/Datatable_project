    public function ssd()
    {
        $data = User::with('city')->get();
        return Datatables::of($data)
            ->addColumn('action', function ($each) {
                return '<a href="#" class="btn btn-danger text-white delete" data-id="' . $each->id . '">Delete</a>';
            })
            ->editColumn('city_id', function ($each) {
                return $each->city->name;
            })
            ->make(true);
    }

